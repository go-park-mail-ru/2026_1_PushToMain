CREATE TABLE emails (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    sender_id BIGINT NOT NULL,

    header TEXT,
    body TEXT,

    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT header_length_check
        CHECK (char_length(header) < 255)
);

CREATE TABLE user_emails (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    user_id BIGINT NOT NULL,
    email_id BIGINT NOT NULL REFERENCES emails(id) ON DELETE CASCADE,

    is_sender BOOLEAN NOT NULL,

    is_read BOOLEAN NOT NULL DEFAULT false,
    is_deleted BOOLEAN NOT NULL DEFAULT false,
    is_starred BOOLEAN NOT NULL DEFAULT false,

    created_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at timestamptz NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT unique_user_email_sender
        UNIQUE (user_id, email_id, is_sender)
);

CREATE INDEX idx_user_emails_receiver_id
    ON user_emails(user_id);

CREATE INDEX idx_user_emails_received_read
    ON user_emails(user_id, is_read);

CREATE INDEX idx_user_emails_receiver_created
    ON user_emails(user_id, created_at);

CREATE INDEX idx_user_emails_receiver_read_created
    ON user_emails(user_id, is_read, created_at DESC);